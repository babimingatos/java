public class DezUm2 {
	/*
	 * Escreva um programa em Java para imprimir os números de 1 (inclusive) a 10
	 * (inclusive) em ordem decrescente
	 */
	public static void main(String args[]) {
		for (int i = 10; i >= 1; i--) {
			System.out.print(i + " ");
		}
		/* Barbara Mingatos */
	}
}
