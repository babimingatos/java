package lista06_fatec;

public class Tabuada24 {

	public static void main(String args[]) {

		/*
		 * Escreva um programa em Java que imprima a tab uada do 1 a o 10 , para os
		 * números de 0 a 10
		 */

		for (int i = 0; i <= 10; i++) {
			System.out.println("1 x " + i + " =" + i * 1 + 
					       "    2 x " + i + " =" + i * 2 +
					       "    3 x " + i + " =" + i * 3 +
					       "    4 x " + i + " =" + i * 4 +
					       "    5 x " + i + " =" + i * 5 +
					       "    6 x " + i + " =" + i * 6 +
					       "    7 x " + i + " =" + i * 7 +
					       "    8 x " + i + " =" + i * 8 +
					       "    9 x " + i + " =" + i * 9 +
					       "    10 x " + i + " =" + i * 10);
		}

		System.out.print("\n---------------\n");
		for (int i = 0; i <= 10; i++) {
		System.out.println("2 x " + i + " = " + i * 2);
		}

		/* Barbara Mingatos */

	}

}
